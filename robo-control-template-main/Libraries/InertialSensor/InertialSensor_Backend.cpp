/***********************************************************************
** 函 数 名： InertialSensor_Backend::correctGyro()
** 函数说明： 修正陀螺仪数据
**---------------------------------------------------------------------
** 输入参数： 无
** 返回参数： 无
***********************************************************************/
void InertialSensor_Backend::correctGyro(void)
{
//	gyro_calib = gyro;
  gyro_calib.x = gyro.x - gyro_offset.x;
  gyro_calib.y = gyro.y - gyro_offset.y;
  gyro_calib.z = gyro.z - gyro_offset.z;
}

